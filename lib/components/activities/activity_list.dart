DataRow _buildListItem(BuildContext context, DocumentSnapshot data) {
  final model = ActivityModel.fromSnapshot(data);
  return DataRow(
      cells: [
    DataCell(Text(model.name)),
    DataCell(Text(model.code)),
        DataCell(Text(model.date)),
        DataCell(Text(model.school)),
        DataCell(Text(model.department.toString())),
        DataCell(Text(model.coordinator.toString())),
        DataCell(Text(model.fees.toString())),
        DataCell(Text(model.classes.length.toString()),onTap: (){

        }),
        DataCell(Row(
          children: [
            IconButton(
              icon: Icon(Icons.delete_forever),
              color: primaryColor,
              onPressed: (){
                AwesomeDialog(
                  dialogBackgroundColor: Colors.white,
                  width: MediaQuery.of(context).size.width*0.3,
                  context: context,
                  dialogType: DialogType.QUESTION,
                  animType: AnimType.BOTTOMSLIDE,
                  title: 'Delete Activity',
                  desc: 'Are you sure you want to delete this record?',
                  btnCancelOnPress: () {
                    Navigator.pushReplacement(context, MaterialPageRoute(builder: (BuildContext context) => ActivityScreen()));
                  },
                  btnOkOnPress: () {
                    FirebaseFirestore.instance.collection('activities').doc(model.id).delete().then((value) =>
                        Navigator.pushReplacement(context, MaterialPageRoute(builder: (BuildContext context) => ActivityScreen())));
                  },
                )..show();
              },
            ),
            IconButton(
              icon: Icon(Icons.edit),
              color: primaryColor,
              onPressed: (){
                _showEdit(context,model);
              },
            ),
          ],
        )),

  ]);
}


