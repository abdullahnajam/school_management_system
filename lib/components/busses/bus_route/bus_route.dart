    List<String> studentPostList=[];
    List<StudentModel> studentData=[];
    final ProgressDialog pr = ProgressDialog(context: context);
    pr.show(max: 100, msg: "Adding");
    for(int i=0;i<selectedStudents.length;i++){
      if(selectedStudents[i].check){
        studentData.add(selectedStudents[i].model);
        studentPostList.add(selectedStudents[i].model.id);
      }
    }
    FirebaseFirestore.instance.collection('bus_route').add({

      'busId': _busId,
      'busNumber': _busController.text,
      'students': studentPostList,

      'driver': _driverController.text,
      'driverId': _driverId,
