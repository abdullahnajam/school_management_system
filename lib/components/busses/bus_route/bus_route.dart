      'datePosted': DateTime.now().millisecondsSinceEpoch,
    }).then((value) {
      for(int i=0;i<studentData.length;i++){
        FirebaseFirestore.instance.collection('fees').add({
          'student':"${studentData[i].firstName} ${studentData[i].lastName}",
          'school': studentData[i].school,
          'itemId': value.id,
          'department': studentData[i].department,
          'studentId': studentData[i].id,
          'parentId': studentData[i].parentId,
          'parent': studentData[i].parent,
          'schoolId': studentData[i].schoolId,
          'dueDate': _dueDateController.text,
          'fromDate': _fromDateController.text,
          'discount': "none",
          'discountId': "none",
          'feeCategoryId': "none",
          'feeCategory': "Bus Fees",
          'fees': int.parse(_feeController.text),
          'academicYear': _academicYearController.text,
          'dueDateInMilli': dueDateInMilli,
