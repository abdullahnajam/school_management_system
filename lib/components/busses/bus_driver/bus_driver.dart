        Map<String, dynamic> data = documentSnapshot.data() as Map<String, dynamic>;
        codes=CodeModel.fromMap(data);
      }
    });
  }
  add() async{
    print("rr");
    final ProgressDialog pr = ProgressDialog(context: context);
    pr.show(max: 100, msg: "Adding");
    FirebaseFirestore.instance.collection('bus_driver').add({
      'name': _nameController.text,
      'phone': _phoneController.text,
      'address': _addressController.text,
      'nationalId': _nationalIdController.text,
      'uniqueId': "bd${codes!.driver}",
      'licenseNumber': _licenseNumberController.text,
      'isArchived': false,
      'datePosted': DateTime.now().millisecondsSinceEpoch,
    }).then((value) {
      int number=codes!.driver;
      number++;
      FirebaseFirestore.instance.collection('key').doc('codes').update({
        'driver':number
      });
      pr.close();
      print("added");
      Navigator.pop(context);
    });
  }

